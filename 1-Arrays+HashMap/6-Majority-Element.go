package main

func majorityElement(nums []int) int {

	majorityElem := nums[0]
	max := 0
	freqMap := make(map[int]int)
	for i := range nums {
		freqMap[nums[i]]++
	}
	for k, v := range freqMap {
		if v > max {
			max = v
			majorityElem = k
		}
	}
	return majorityElem
}

